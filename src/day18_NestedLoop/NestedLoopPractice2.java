package day18_NestedLoop;

public class NestedLoopPractice2 {
    public static void main(String[] args) {
        for (int j = 0; j < 10; j++) {  // when this loop executed 1 time, other loop is going to
                                        // write * ten times.
            for (int i = 0; i < 10; i++) {
                System.out.print("* ");
            }
            System.out.println();
        }










    }
}
